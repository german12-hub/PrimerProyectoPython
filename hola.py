# Variables
nombre = "Germán"
edad = 32
es_programador = True

print("Hola,", nombre)
print("Edad:", edad)
print("¿Es programador?", es_programador)

# Operadores
x = 10
y = 3

print("Suma:", x + y)
print("Resta:", x - y)
print("Multiplicación:", x * y)
print("División:", x / y)
print("División entera:", x // y)
print("Potencia:", x ** y)

# Tipos de datos
print(type(nombre))  # str
print(type(edad))    # int
print(type(es_programador))  # bool
